    //array contains unit elements
    private String[] units = {"Meter", "Mile", "Inch", "Hectometre", "Decameter"};
    private double milestoMeters = 1609.34;// 1 mile= 1609.34*meter
    private double inchestoMeters = 0.0254;//1 inch=0.0254*meter
    private double hectometreToMeters = 100;
    private double decameterToMeters = 10;

