    /**
     * add element to combobox
     */
    private void addElementtoCbx() {
        //iterate over the unit element in array
        for (String s : units) {
            cbxFrom.addItem(s);//add element to cbxFrom
            cbxTo.addItem(s);//add element to cbxTo
        }
    }

    /**
     * Convert the units
     */
    private void convert() {

        //get data from cbx
        String unitFrom = cbxFrom.getSelectedItem().toString();
        String unitTo = cbxTo.getSelectedItem().toString();
        double tempFrom = 1;
        double tempTo = 1;
        double valueFrom;

        //parse data to double value
        try {
            valueFrom = Double.parseDouble(txtFrom.getText());
        } catch (NumberFormatException e) {
            txtTo.setText(""); //clear textfiled to enter again
            return;
        }
        //if 2 combobox equals variable
        if (unitFrom.equals(unitTo)) {
            txtTo.setText(Double.toString(valueFrom));
        }
        switch (unitFrom) {

            //1*mile=1609.34*meter;
            case "Mile":
                tempFrom = milestoMeters;
                break;

            //1*inch=0.0254*meter
            case "Inch":
                tempFrom = inchestoMeters;
                break;
            case "Hectometre":
                tempFrom = hectometreToMeters;
                break;
            case "Decameter":
                tempFrom = decameterToMeters;
                break;
        }
        switch (unitTo) {

            //1*mile=1609.34*meter;
            case "Mile":
                tempTo = milestoMeters;
                break;

            //1*inch=0.0254*meter
            case "Inch":
                tempTo = inchestoMeters;
                break;
            case "Hectometre":
                tempTo = hectometreToMeters;
                break;
            case "Decameter":
                tempTo = decameterToMeters;
                break;
        }

        //caculate valueTo
        double valueTo = valueFrom * tempFrom / tempTo;
        //format number
        DecimalFormat df = new DecimalFormat("0.####");
        //display value
        txtTo.setText(df.format(valueTo));
    }

