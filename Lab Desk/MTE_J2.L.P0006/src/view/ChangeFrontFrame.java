    private void listSizeValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listSizeValueChanged
        //get number size
        int fontSize = (int) listSize.getSelectedValue();
        txtSize.setText(fontSize + "");
        //set font for txt Preview
        txtPreview.setFont(new Font(listFontFamily.getSelectedValue(), listFontStyle.getSelectedIndex(), fontSize));
    }//GEN-LAST:event_listSizeValueChanged

    private void listFontStyleValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listFontStyleValueChanged
        //get current font style
        int fontStyle = listFontStyle.getSelectedIndex();
        txtFontStyle.setText(listFontStyle.getSelectedValue());
        //set font for txt Preview
        txtPreview.setFont(new Font(listFontFamily.getSelectedValue(), fontStyle, (int) listSize.getSelectedValue()));
    }//GEN-LAST:event_listFontStyleValueChanged

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        try {
            //get font for txt Preview set for text area in main screen
            Font curFont = txtPreview.getFont();
            textEditorFrame.getTxtDisplay().setFont(curFont);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Size is invalid");
        }
    }//GEN-LAST:event_btnOKActionPerformed

