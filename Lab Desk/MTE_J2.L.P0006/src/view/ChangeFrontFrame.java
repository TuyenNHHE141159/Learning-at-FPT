    /**
     * load item font Family to list
     */
    public void loadFontFamily() {

        // get all FontFamilyNames to array fontNames
        String[] fontNames = textEditorController.getFontFamily();
        DefaultListModel listModel = new DefaultListModel();
        // add all elements in array fontNames to list model
        for (String fontName : fontNames) {
            listModel.addElement(fontName);
        }
        //add list font to Jlist font
        listFontFamily.setModel(listModel);
    }

    /**
     * load all font size
     *
     * @no return
     * @no param
     */
    public void loadListSize() {
        DefaultListModel listModel = new DefaultListModel();
        for (int i = 8; i <= 48; i += 2) {
            listModel.addElement(i);
        }
        // add list size to jList size
        listSize.setModel(listModel);
        listSize.setSelectedIndex(0);
    }

