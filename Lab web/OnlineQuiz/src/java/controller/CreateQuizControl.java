    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
      //  processRequest(request, response);
        request.getRequestDispatcher("MakeQuizPage.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
         request.setCharacterEncoding("UTF-8");
          try {
            String question = request.getParameter("question");
            String option1 = request.getParameter("option1");
            String option2 = request.getParameter("option2");
            String option3 = request.getParameter("option3");
            String option4 = request.getParameter("option4");
            String answers[] = request.getParameterValues("answer"); 
            String myanswer = "";
            for (String str : answers) {
                myanswer += str + ",";
            }
            myanswer = myanswer.substring(0, myanswer.length() - 1);
            Date now = new Date();
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String date = df.format(now);

            HttpSession session = request.getSession();
            User u = (User) session.getAttribute("user");
            int uid = u.getId();

            DAO dao = new DAO();
            dao.makeQuiz(question, option1, option2, option3, option4, myanswer, date, uid);
            request.getRequestDispatcher("StartQuizPage.jsp").forward(request, response);
        } catch (Exception e) {
        }
    }

